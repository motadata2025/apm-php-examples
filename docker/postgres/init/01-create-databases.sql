-- PostgreSQL Database Initialization for APM PHP Examples
-- Creates databases for all applications

-- Create databases for each application
CREATE DATABASE simple_php_db;
CREATE DATABASE slim_app_db;
CREATE DATABASE symfony_app_db;
CREATE DATABASE codeigniter_app_db;
CREATE DATABASE laravel_app_db;

-- Connect to each database and create users table
\c simple_php_db;
CREATE TABLE IF NOT EXISTS users (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) UNIQUE NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

