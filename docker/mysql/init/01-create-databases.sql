-- Grant privileges
GRANT ALL PRIVILEGES ON simple_php_db.* TO 'root'@'%';
GRANT ALL PRIVILEGES ON slim_app_db.* TO 'root'@'%';
GRANT ALL PRIVILEGES ON symfony_app_db.* TO 'root'@'%';
GRANT ALL PRIVILEGES ON codeigniter_app_db.* TO 'root'@'%';
GRANT ALL PRIVILEGES ON laravel_app_db.* TO 'root'@'%';

FLUSH PRIVILEGES;
